import java.util.*;
class yp_addtorem
{
   public static void main(String args[])
   {
     int a,b,add,sub,multi,div,rem;
	 System.out.println("\n Enter Two Numbers");
	 Scanner scan=new Scanner(System.in);
	 a=scan.nextInt();
	 b=scan.nextInt();
	 add=a+b;
	 sub=a-b;
	 multi=a*b;
	 div=a/b;
	 rem=a%b;
	 System.out.println("\n a= "+a);
	 System.out.println("\n b= "+b);
	 System.out.println("\n add= "+add);
	 System.out.println("\n sub=  "+sub);
	 System.out.println("\n multi= "+multi);
	 System.out.println("\n div= "+div);
	 System.out.println("\n rem= "+rem);
	 }
}
	 